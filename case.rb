weather = "rain"

case weather
  when 'sunny'
    puts "You should wear some sunscreen."
  when 'snowing'
    puts "Put on your snowboots and heavy coat."
  when 'foggy'
    puts "Wear a light jacket."
  when 'cloudy'
    puts "Better bring an umbrella just in case."
  when 'hailing'
    puts 'wear your helmet'
  else
    puts "It's best to prepare for everything and anything."
end
